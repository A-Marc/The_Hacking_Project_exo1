# #{} permet d'intégrer un calcul/un test logique dans un puts ou un print

#Annonce le but de l'exercice
puts "On va compter le nombre d'heures de travail à THP"
#Annonce, calcul et affiche le nombre d'heure
puts "Travail : #{10 * 5 * 11}"
#ANnonce, calcul et affiche le nombre de minutes
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"
#Annonce le nombre de seconde
puts "Et en secondes ?"
#Calcul et affiche le nombre de seconde
puts 10 * 5 * 11 * 60 * 60
#Annonce un test logique
puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"
#Effectue et annonce le résultat du test logique
puts 3 + 2 < 5 - 7
#Annonce, effectue et affiche le résultat d'un calcul
puts "Ça fait combien 3 + 2 ? #{3 + 2}"
#Annonce, effectue et affiche le résultat d'un calcul
puts "Ça fait combien 5 - 7 ? #{5 - 7}"
#Affiche du texte
puts "Ok, c'est faux alors !"
#Affiche du texte
puts "C'est drôle ça, faisons-en plus :"
#Annonce, effectue et affiche le résultat d'un test logique
puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"
#Annonce, effectue et affiche le résultat d'un test logique
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"
#Annonce, effectue et affiche le résultat d'un test logique
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"